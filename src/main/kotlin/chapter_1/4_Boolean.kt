fun main() {
    val num1: Int = 1
    val num2: Int = 2
    println(num1 > num2)    // 输出 false
    println(num1 < num2)    // 输出 true
    println(num1 == num2)   // 输出 false
    println(num1 != num2)   // 输出 true
}